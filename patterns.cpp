// -----------------------------------------------------------------------------------------------------------------
/*

Pattern - 1
n=5
* * * * *
* * * * *
* * * * *
* * * * *
* * * * *

*/


void pattern1(){

    int n;
    cin>>n;

    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            cout<<"* ";
        }
        cout<<endl;
    }

}




